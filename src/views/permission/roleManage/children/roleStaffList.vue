        searchKeyWords: '' // 搜索关键词
      }
    },
    methods: {
      getRoleStaffListAjax () {
        this.$http
          .post('/api/role/roles/loadRolesToEmployeeInfo', {roleId: this.role.id})
          .then(res => {
            if (res.data.code === '0') {
              // 初始化数据
              this.roleStaffList = res.data.data
              // 拷贝数组
              this.copy(this.roleStaffList, this.tmpRoleStaffList)
            } else {
              this.$message({ type: 'error', message: res.data.msg })
            }
          })
          .catch(err => {
            this.$message({ type: 'error', message: err.msg })
          })
      },
      showBody () { // 显示body
        this.$refs.slideRightBody.style.right = 0
        this.$refs.slideRightBody.style.opacity = 1
      },
