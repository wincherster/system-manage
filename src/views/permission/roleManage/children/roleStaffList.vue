        this.$emit('close')
      },
      filterValidState (value, row) { // 有效/无效状态过滤
        console.log(value, row)
        return row.state === value
      },
      search () {
        if (this.searchKeyWords.trim() !== '') {
          let searchResult = []
          for (let i = 0; i < this.roleStaffList.length; i++) {
            let emp = this.roleStaffList[i]
            if (emp.empName === this.searchKeyWords || emp.empAccount === this.searchKeyWords) {
              searchResult.push(emp)
            }
          }
          this.tmpRoleStaffList = searchResult
        } else {
          this.tmpRoleStaffList = this.roleStaffList
        }
      },
      copy (srcArr, distArr) { // 数组拷贝
        for (let i = 0; i < srcArr.length; i++) {
          let o = srcArr[i]
          let tmp = {}
          for (let k in o) {
            tmp[k] = o[k]
          }
          distArr.push(tmp)
        }
      }
    }
